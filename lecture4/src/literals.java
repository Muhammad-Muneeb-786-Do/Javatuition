public class literals{
    public static void main(String[] args) {
        //literals

        int binarynumber = 0b101011; //binary number
        int hexnumber = 0x12F; //hexadecimal number

        double dabal = 90; // it will autotically convert integer to doubel.
        float flat = 12.0f; //to assign floating point number we have to explicity mention them.

        char character = 'a';
        character++; // it will make the character b.

        System.out.println(binarynumber);
        System.out.println(hexnumber);
        System.out.println(dabal);
        System.out.println(flat);
        System.out.println(character);




        
    }
}